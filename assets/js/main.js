document.addEventListener('DOMContentLoaded', function() {
    const rssUrl = 'https://rss.nytimes.com/services/xml/rss/nyt/HomePage.xml'; // URL del feed RSS
    const newsContainer = document.getElementById('news-container');

    fetch(rssUrl)
        .then(response => response.text())
        .then(data => {
            const parser = new DOMParser();
            const xml = parser.parseFromString(data, 'application/xml');
            const items = xml.querySelectorAll('item');

            items.forEach(item => {
                const title = item.querySelector('title').textContent;
                const description = item.querySelector('description').textContent;
                const link = item.querySelector('link').textContent;

                const newsItem = document.createElement('div');
                newsItem.classList.add('news-item');

                const newsTitle = document.createElement('h2');
                newsTitle.textContent = title;

                const newsDescription = document.createElement('p');
                newsDescription.textContent = description;

                const newsLink = document.createElement('a');
                newsLink.href = link;
                newsLink.textContent = 'Leer mÃ¡s';
                newsLink.target = '_blank';

                newsItem.appendChild(newsTitle);
                newsItem.appendChild(newsDescription);
                newsItem.appendChild(newsLink);

                newsContainer.appendChild(newsItem);
            });
        })
        .catch(error => console.error('Error al cargar el feed RSS:', error));
});

